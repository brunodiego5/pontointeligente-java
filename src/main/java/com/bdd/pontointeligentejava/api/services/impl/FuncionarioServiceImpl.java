package com.bdd.pontointeligentejava.api.services.impl;

import java.util.Optional;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import com.bdd.pontointeligentejava.api.entities.Funcionario;
import com.bdd.pontointeligentejava.api.repositories.FuncionarioRepository;
import com.bdd.pontointeligentejava.api.services.FuncionarioService;

@Service
public class FuncionarioServiceImpl implements FuncionarioService {
	
	private static final Logger log = LoggerFactory.getLogger(FuncionarioServiceImpl.class);
	
	@Autowired
	private FuncionarioRepository funcionarioRepository;

	@Override
	public Funcionario persistir(Funcionario funcionario) {
		log.info("Persistindo o funcion치rio: {} ", funcionario);
		return this.funcionarioRepository.save(funcionario);
	}

	@Override
	public Optional<Funcionario> buscarPorCpf(String cpf) {
		log.info("Buscando o funcion치rio pelo cpf {} ", cpf);
		return Optional.ofNullable(this.funcionarioRepository.findByCpf(cpf));
	}

	@Override
	public Optional<Funcionario> buscarPorEmail(String email) {
		log.info("Buscando o funcion치rio pelo email {} ", email);
		return Optional.ofNullable(this.funcionarioRepository.findByEmail(email));
	}

	@Override
	public Optional<Funcionario> buscarPorId(Long id) {
		log.info("Buscando o funcion치rio pelo id {} ", id);
		// return Optional.ofNullable(this.funcionarioRepository.findOne(id)); <= para Spring 1.5
		return this.funcionarioRepository.findById(id);
	}

}
